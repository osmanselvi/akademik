<p class="">Dergimize gönderilecek makaleler Microsoft Word dosyasında gönderilmelidir.</p> 
<p>Dergimize gönderilecek makaleler aşağıdaki “Makale Şablonu" üzerinde hazırlanmalıdır.</p>
<a href="belgeler/makale_sablon.docx">Makale Şablonu</a>
<p>Her makalede Türkçe ve İngilizce olmak üzere iki başlık olmalıdır. Üstte Türkçe başlık olmalı ve bağlaçlar hariç tüm harfler büyük harfle koyu yazılmalıdır. Türkçe başlığın hemen altında İngilizce başlık yer almalı ve başlıktaki kelimelerin birinci harfleri büyük, koyu ve italik olmalıdır.<br/>
Yayınlanmak üzere gönderilen makalelerde en az 150 en fazla 250 kelimeden oluşan Türkçe ve İngilizce öz yer almalıdır. Özlerin altında ise 3 ila 5 kavram arasında anahtar kelimeler bulunmalıdır. <br/>
Makale içerinde yer alan başlıkların ilk harfi büyük olmalı ve koyu yazılmalıdır. Paragraf girintisi ile aynı hizada yer almalıdır. Başlıklardan önce ve sonra 6 nk boşluk bırakılmalıdır. <br/>
Edebiyat Bilimleri Dergisi’ne gönderilecek makaleler aşağıdaki sayfa düzenine ve özelliklere uygun hazırlanmalıdır:</p>
<p><b>Sayfa Düzeni:</b>
 A4 Dikey, Üst, alt ve sağ kenar boşlukları 2,5 cm, sol kenar boşluğu 3 cm olmalıdır. </p>
 <p><b>Font:</b> 
Palatino Linotype, Ana başlıklar 11, Metin 10 punto büyüklüğünde yazılmalıdır. (Microsoft Word’de yer almayan bir font kullanılmış ise bu font makale dosyası ile birlikte dergimize gönderilmelidir.)</p>
<p><b>Paragraf Aralığı:</b> Önce 6 nk – sonra 0 nk</p>
<p><b>Paragraf Girintisi: </b>
1,25 paragraf girintisi</p>
<p><b>Satır Aralığı:</b>
 1.15 satır aralığı (Dip notlarda tek satır aralığı)</p>
 <p><b>Sayfa numarası:</b>
Makale dosyasına sayfa numarası eklenmemelidir. </p>
<p><b>Dipnot:</b> Dipnotların tamamı 9 punto, 1 satır aralığında, 0 nk paragraf aralığında yazılmalıdır. </p>
<br><p>Yazıda kullanılan noktalama işaretleri kendinden önceki sözcüğe bitişik kullanılmalı ve noktalama işaretlerinden sonra bir boşluk bırakılmalıdır. </p>

</p>Makale içinde kullanılan görsel, tablo ve şekil sayfa yapısının dışına taşmamalıdır. Kullanılan görsel ve şekillerin görünürlüğü ve kalitesi yüksek olmalıdır. </p>
</p>Makalede sözcüklerin yazımı, noktalama işaretleri ve kısaltmamaların kullanımında güncel Türk Dil Kurumu Yazım Kılavuzu esas alınmalıdır. </p>
<p>Makalede ek şekil, grafik, tablo ve görseller kaynakça sonrasında yeni bir sayfada verilmelidir. Eklerden önce başlık eklenmelidir. (Örnek: Ek 1., Ek 2.) Verilen ekin açıklaması yapılmalıdır. (Tablo 1., Şekil 1., Görsel 1. vb.) Şekil, grafik ve tablo düzenlenebilir bir formatta ise başlık ve iç metinler 10 punto olmalıdır. </p>
<p>Dergiye gönderilecek makaleler İntihal Programında taranmalıdır. Rapordaki benzerlik indeksinin %20’nin altında olması gerekmektedir. Benzerlik Raporu makaleyle birlikte gönderilmelidir. </p>
<p>Makale ile beraber makale eğer tek yazarlı ise <a href="belgeler/tekyazarli_telifhakkisozlesme.docx">Tek Yazarlı Yayın Hakkı Devir Formu</a>, çok yazarlı ise <a href="belgeler/cokyazarli_telifhakkisozlesme.docx">Çok Yazarlı Yayın Hakkı Devir Formu</a> doldurulup imzalı bir şekilde gönderilmesi gerekmektedir. </p>
<p>Hakemlik süreci başlatılacak olan makalenin içerisinde ve dosya adında yazar isimlerine kesinlikle yer verilmemelidir. Hakem süreçleri olumlu tamamlanan yazılar için yazardan son hâli talep edilirken kişisel bilgilerini girmeleri istenecektir.</p>

